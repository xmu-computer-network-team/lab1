# common/crc.py
#
# 【任务说明】
# 本模块提供 CRC（循环冗余校验）的计算函数。
# 编码端用它给数据"盖章"，解码端用它验证数据有没有被读错。
#
# ============================================================
# 【背景知识：CRC 是什么】
#
# CRC 本质是一种"数据指纹"。对一段数据算出一个固定长度的校验值，
# 数据哪怕只改了 1 个 bit，校验值就会完全不同。
#
# 类比：把一段数据当作一个巨大的数字，除以一个固定的"生成多项式"，
# 取余数作为校验值。发送方把余数附在数据后面，接收方重新算一遍余数，
# 如果和附带的值不一样，说明数据在传输中出了错。
#
# 本项目用到两种 CRC：
#
# 1. CRC-32（用于数据段校验）
#    - 生成多项式: 0x04C11DB7（IEEE 802.3 标准，以太网、ZIP、PNG 都用这个）
#    - 输出: 32 位整数（4 字节）
#    - 未检出错误的概率: 约 1/4,294,967,296（约 1/43 亿）
#    - Python 标准库 zlib.crc32() 直接可用，不需要自己实现算法
#
# 2. CRC-8（用于帧头校验）
#    - 生成多项式: 0x07（CRC-8/ITU 标准）
#    - 输出: 8 位整数（1 字节）
#    - 未检出错误的概率: 约 1/256
#    - Python 没有内置实现，需要自己写
#    - 帧头只有 29 bits 数据（奇偶1 + 帧号12 + 长度16），用 CRC-8 够了
#
# 【为什么不统一用 CRC-32】
# 帧头很短（29 bits），CRC-32 的 32 bits 校验值比数据本身还长，浪费空间。
# CRC-8 的 8 bits 对于 29 bits 的帧头已经足够可靠。
#
# ============================================================
#
# 【在项目中的调用关系】
#
# 编码端:
#   data_formatter.py → crc32(chunk_bytes) → 附在每个 152 bits 数据段尾部
#   frame_builder.py  → crc8(header_bits)  → 附在 37 bits 帧头尾部
#
# 解码端:
#   data_recovery.py → crc32(payload_bytes) → 和读到的 CRC 比对，不一致则丢弃
#   data_recovery.py → crc8(header_bits)    → 和读到的 CRC 比对，不一致则跳帧
#
# ============================================================
#
# 【依赖】
# - zlib（Python 内置，用于 crc32）
# - 无第三方依赖
#
# 【验收标准】
# 运行 test/crc_test.py 全部通过即合格


